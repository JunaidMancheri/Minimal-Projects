const {Schema, model} = require('mongoose');

const address = new Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  address: {
    type: String,
    required: true,
    trim: true,
  },
  city: {
    type: String,
    required: true,
    trim: true,
  },
  state: {
    type: String,
    required: true,
    trim: true,
  },
  zip: {
    type: String,
    required: true,
    trim: true,
  },
  country: {
    type: String,
    required: true,
    trim: true,
  },
  phone: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },

});

const addressSchema = new Schema({
  _id: {
    type: Schema.Types.ObjectId,
    required: true,
    ref: 'users',
  },
  addresses: [address],
});

module.exports = model('addresses', addressSchema);

